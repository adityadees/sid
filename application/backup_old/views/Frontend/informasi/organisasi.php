                                                <h5>PENDAHULIAN</h5>
                                                Pendidikan merupakan upaya untuk menuntun kekuatan kodrat pada diri setiap anak agar mereka mampu tumbuh dan berkembang sebagai manusia maupun sebagai anggota masyarakat yang bisa mencapai keselamatan dan kebahagiaan dalam hidup mereka (Ki Hajar Dewantoro). Pendidikan adalah usaha sadar dan terencana untuk mewujudkan suasana belajar dan proses pembelajaran agar peserta didik secara aktif mengembangkan potensi di dalam diri untuk memiliki kekuatan spritual keagamaan, pengendalian diri, kepribadian, kecerdasan, akhlak mulia, serta keterampilan yang diperlukan dirinya, masyarakat, bangsa, dan negara. (UU No.20 Tahn 2003).

                                                Pendidikan bisa didapat melalui 2 hal, yaitu pendidikan formal dan non formal.
                                                Pendidikan formal diperoleh dengan mengikuti progam yang terstruktur dan terencana oleh badan kepemerintahan, misalnya sekolah.
                                                Pendidikan non formal diperoleh dengan menjalani aktivitas kehidupan sehari-hari yang tidak terikat oleh badan kepemerintahan, misalnya belajar melalui pengalaman, belajar sendiri melalui buku-buku bacaan, dan belajar dari pengalaman orang lain.

                                                Pelatihan adalah suatu usaha untuk meningkatkan pengetahuan dan kemampuan seseorang dalam melaksanakan pekerjaan lebih efektif dan efisien. Pelatihan adalah serangkaian program yang dirancang untuk meningkatkan pengetahuan dan kemampuan seseorang dalam hubungannya dengan pekerjaannya.


                                                Unit usaha Pendidikan dan Pengajaran merupakan unit usaha dibawah koordinasi pada Badan Pengelola Usaha Universitas Sriwijaya. Universitas Sriwijaya memiliki kepakaran sumber daya manusia yang handal dan komitmen terhadap lembaga, yang dapat digunakan oleh pihak eksternal sebagai Tenaga Ahli sesuai bidangnya masing-masing. 
